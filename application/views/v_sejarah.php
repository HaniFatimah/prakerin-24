        <h2>Sejarah dan Perkembangan Internet of Things</h1>
        <br>
        <p>
        Perkembangan teknologi pada zaman sekarang yang semakin pesat, memungkinkan di masa yang akan mendatang penggunaan komputer mendominasi pekerjaan manusia dan mengalahkan kemampuan komputasi manusia seperti mengontrol peralatan elektronik, karena dengan IOT (Internet of Things) dapat mengontrol peralatan elektronik dari jarak jauh dengan menggunakan media internet. Perkembangan teknologi tersebut memiliki sejarah yang cukup  panjang. Berikut adalah sejarah singkat mengenai perkembangan IOT :
        </p>
        <p>
        Sejak internet mulai dikenal pada tahun 1989, mulai banyak kegiatan yang yang dilakukan / dikerjakan melalui internet, berikut  adalah contoh beberapa  kegiatan tersebut:
        </p>
        <p>
        Pada tahun 1990, John Ramkey bekerja sama dengan temannya  Simon Hackett  menciptakan ‘perangkat’ Pemanggang roti yang terhubung ke Internet dengan jaringan TCP / IP, dan dikendalikan dengan Basis Informasi Manajemen Protokol Manajemen Jaringan Sederhana (SNMP MIB), dengan  memiliki satu kontrol, untuk menyalakan daya, dan kegelapan roti panggang dikendalikan oleh berapa lama daya disimpan,namun manusia tetap harus memasukan roti ke dalam perangkat tersebut. Pada 1999 perkembangan perangkat ini dikembangkan  dengan menambahkan interop  yang merupakan  robot derek kecil  yang juga dapat dikendalikan melalui internet, yang memungkinkan mengambil roti dan menjatuhkan ke dalam perangkat tersebut, sehingga dapat membuat otomatisasi mesin dari end to end.
        </p>
        <p>
        Tahun 1999, Kevin Ashton menciptakan The Internet of Things, direktur eksekutif Auto IDCentre, MIT. Mereka juga menemukan peralatan berbasis RFID (Radio Frequency Identification) global yang sistem identifikasi pada tahun yang sama. Penemuan ini disebut sebagai sebuah lompatan besar dalam commercializing IoT.
        </p>
        <p>
        Tahun 2000 LG (Lucky and Goldstar) yang merupakan  perusahaan multinasional dari korea selatan mengumumkan rencananya menciptakan kulkas pintar yang akan menentukan sendiri apakah bisa atau tidak makanan yang tersimpan di dalamnya diisi ulang.
        </p>
        <p>
        Pada tahun 2003 RFID mulai ditempatkan pada tingkat besar besaran di militer AS di Program Savi mereka. Pada tahun yang sama melihat raksasa ritel Walmart untuk menyebarkan RFID di semua toko toko di seluruh dunia untuk lebih besar batas.
        </p>
        <p>Pada tahun 2005 arus publikasi utama seperti The Guardian, Amerika ilmiah dan Boston Globe mengutip banyak artikel tentang IOT.
            Pada tahun 2008 kelompok perusahaan meluncurkan IPSO Alliance untuk mempromosikan penggunaan Internet Protocol (IP) dalam jaringan dari “Smart object” dan untuk mengaktifkan Internet of Things</p>
        <p>
        Pada tahun 2008 FCC menyetujui penggunaan “white space spectrum”. Akhirnya peluncuran IPv6 di tahun 2011 memicu pertumbuhan besar di bidang Internet of Things, perkembangan ini didukung oleh perusahaan besar seperti Cisco, IBM, Ericson mengambil inisiatif banyak dari pendidikan dan komersial dengan IOT teknologi dapat hanya dijelaskan sebagai hubungan antara manusia dan komputer.
        </p>
            <br>
<hr>
        <h3>Perkembangan IoT di indonesia</h3>
        <br>
        <p>
        Perkembangan teknologi informasi mendorong perubahan zaman yang semakin pesat. Pada era revolusi industri 4.0, kita mungkin sering mendengar istilah Internet of Things (IoT). Kedepannya, potensi market place dari IoT ini akan semakin besar. Untuk itu pemerintah sendiri telah mencanangkan program Making Indonesia 4.0 yang salah satu kompenennya ialah IoT. 
        </p>
        <p>
        IoT sendiri dapat diartikan sebagai aktivitas antara manusia dan benda (things), benda dengan benda,  seperti sensor, robot, platform, dan cloud yang terhubung melalui protokol komunikasi standar untuk saling menerima atau mengirimkan informasi sehingga memungkinkan proses kerja tertentu menjadi lebih efisien.
        </p>
        <p>
        Menurut Prof.Dr.Ir. Suhono Harso Supangkat M.Eng., Guru Besar pada KK Teknologi Informasi, Sekolah Teknik Elektro dan Informatik ITB, potensi IoT di Indonesia sangatlah besar. Namun tentu saja, selain ada peluang juga ada tantangan yang harus dihadapi, misalnya masalah pemerataan infrastruktur jaringan dan kondisi geografi.
        </p>
        <p>
        Dia menerangkan, ada sejumlah kompenen dalam pengembangan IoT yang sangat berpengaruh, pertama adalah manusia. Komopnen ini mencakup SDM yang mampu mendukung pengembangan IoT, dampak IoT bagi dunia kerja di masa depan, dan perubahan budaya yang diakibatkan oleh kehadiran IoT.
        </p>
        <p>
        Kemudian yang kedua adalah teknologi. Komponen ini mencakup bagaimana benda-benda seperti sensor, cloud, platform, robot dan lainnya terhubung melalui protokol dan algoritma tertentu. Dan ketiga adalah tata kelola. Dalam aspek ini komponennya mencakup regulasi, model bisnis, dan pengembangan peran antar masing-masing pihak yang berkepentingan dalam pengembangan IoT di Indonesia.
        </p>
