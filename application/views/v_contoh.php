        <h3><b>Inilah Beberapa Contoh Ide Project untuk Internet of Thing</b></h3>
        <hr>
        <p>Ada banyak contoh project <b>Penerapan Internet of Thing</b> yang bisa Anda buat. Berikut ini adalah beberapa contoh ide project dari beberapa kalangan yang mengembangkan Internet of Thing untuk mempermudah aktivitas sehari-hari.</p>
        <p><b>1. Colokan Listrik Pintar</b></p>
        <img  style="height:40%; width:40%;"src="<?php echo base_url('./assets/img/colokan.png') ?>" class="card-img" alt="...">
        <br><br><p>Ide project untuk Internet of Thing yang pertama adalah pembuatan colokan listrik pintar. Colokan listrik yang dimiliki dikoneksikan dengan Wi-Fi dan disambungkan dengan barang-barang elektronik seperti lampu atau oven. Jadi, Anda nantinya bisa menghidupkan maupun mematikan sambungan listrik dengan jangkauan internet pada ponsel yang terkoneksi ke colokan listrik tersebut. Dengan cara seperti ini, ketika berada di luar rumah dan lupa mematikan lampu atau barang elektronik, Anda hanya perlu membuka ponsel dan mematikan koneksi listriknya via jarak jauh.</p>

        <p><b>2. Smart Garage Door</b></p>
        <img  style="height:40%; width:40%;"src="<?php echo base_url('./assets/img/garage.png') ?>" class="card-img" alt="...">
        <br><br><p>Seperti namanya, project Smart Garage Door merupakan project IoT yang dapat memonitor status pintu garasi sekaligus membukanya dengan fitur scanner sidik jari. Apabila menerapkan project ini, Anda bisa memonitor garasi dimanapun berada melalui ponsel. Untuk akses pembukanya juga akan lebih aman karena harus melalui scan sidik jari yang ada di ponsel Anda. Cara ini akan menginformasikan Anda yang tidak tahu ketika ada akses paksa dari pihak-pihak yang berusaha masuk ke garasi.</p>

        <p><b>3. Kulkas Pintar</b></p>
        <img  style="height:40%; width:40%;"src="<?php echo base_url('./assets/img/kulkas.png') ?>" class="card-img" alt="...">
        <br><br><p>Salah satu ide project untuk Internet of Thing adalah penerapan kulkas pintar. Biasanya, ketika ingin melihat persediaan makanan atau lainnya di kulkas, kita harus melihat langsung isi dalam kulkas. Namun, dengan inovasi kulkas pintar, Anda bisa melacak persediaan apa yang menipis atau telah habis melalui internet smartphone yang terhubung dengan kulkas tersebut. Jadi, dengan melihat notifikasi, Anda bisa segera berbelanja untuk mensuplai kembali persediaan yang hampir habis.</p>

        <p><b>4. Smart Trash Can</b></p>
        <img  style="height:40%; width:40%;"src="<?php echo base_url('./assets/img/smart.png') ?>" class="card-img" alt="...">
        <br><br><p>Tempat sampah pintar merupakan bentuk inovasi lainnya yang bisa Anda jadikan sebagai referensi. Dengan alat ini, Anda bisa mengoptimalkan pengumpulan sampah dan meminimalisir konsumsi bahan bakar. Sistem pada tempat sampah sendiri akan memberikan notifikasi mengenai jumlah dalam bak tersebut serta suhu bak. Inovasi ini tentu akan sangat berguna untuk mereka yang bekerja mengumpulkan sampah serta semua warga kota secara keseluruhan.</p>

        <p><b>5. Sistem Peringatan Bahaya</b></p>
        <p>Di latar belakangi oleh rasa was-was sebagian orang ketika meninggalkan rumahnya, beberapa kalangan menciptakan sistem peringatan bahaya yang dapat memberikan notifikasi sensor ke ponsel pemiliknya via internet. Ide project untuk Internet of Things dapat Anda terapkan dengan buat alat sendiri atau dengan memanfaatkan perusahaan yang bisa membuat project ini. Namun, di rumah Anda perlu memasang sensor-sensor seperti sensor suhu, gerakan, dan sebagainya sehingga bisa terhubung ke alat peringatan bahaya.</p>

        <p><b>6. IoT Doorbell</b></p>
        <p>Sesuai namanya, teknologi internet yang ada pada ponsel dihubungkan dengan perangkat pada bel pintu rumah. Dengan project ini, Anda bisa memantau siapa saja yang datang bertamu ke rumah ketika si empunya sedang bepergian. Notifikasi yang dikirimkan ke ponsel bisa berupa sms atau email.</p>

        <p><b>7. Alat IoT untuk memonitor dan memanajemen kesehatan</b></p> 
        <img  style="height:40%; width:40%;"src="<?php echo base_url('./assets/img/sehatan.png') ?>" class="card-img" alt="...">
        <br><br><p>Teknologi internet of things juga banyak diimplementasikan dalam bidang kesehatan. Beberapa orang membuat inovasi di bidang kesehatan dengan membuat alat yang dapat memantau kesehatan pasien jarak jauh. Data dari sensor yang terpasang pada pasien nantinya akan ditransfer pada operatornya melalui internet. Dengan begitu, dokter bisa menganalisis kesehatan pasien dan menyarankan praktik klinis ketika terjadi hal-hal yang tidak diinginkan.</p>

        <p><b>8. Alarm Anti Lupa</b></p>
        <p>Bagi Anda yang sering lupa untuk mengurus banyak hal saat meninggalkan rumah, inovasi Internet of Things ini harus dimiliki. Alarm Anti Lupa akan mengingatkan Anda melalui aplikasi ponsel sehingga Anda tahu pekerjaan apa saja yang belum diselesaikan. Jadi, kelupaan seperti memberi makan peliharaan, memastikan semua pintu terkunci, dan sebagainya tidak akan terjadi karena Anda memiliki alarm IoT ini.</p>

        <p><b>9. IoT Botol Pi</b></p>
        <p>Selain di bidang rumah tangga, ide project untuk Internet of Thing juga merambah ke bidang kesehatan. Salah satunya adalah dengan diterapkannya IoT botol pil yang akan sangat membantu pasien dan dokternya. Jadi, pada botol obat yang dimiliki pasien akan dilengkapi dengan tombol khusus pada tutupnya. Tombol tersebut dapat mendeteksi pola waktu pengambilan pil oleh peminumnya. Selain itu, dokter juga bisa mendapat notifikasi status ketersediaan pil dalam botol tersebut karena adanya sensor IR.</p>

        <p><b>10. Mesin Penyiram Tanaman Otomatis</b></p>
        <img  style="height:40%; width:40%;"src="<?php echo base_url('./assets/img/siram.png') ?>" class="card-img" alt="...">
        <br><br><p>Inovasi berikutnya yang mungkin bisa menginspirasi Anda adalah mesin penyiram tanaman otomatis. Dengan alat ini, Anda bisa menentukan jadwal penyiraman agar mesin secara otomatis melakukan penyiraman. Alat yang dikembangkan oleh Chris Anderson dan Rui Wang ini bisa dibuat sendiri dengan menghubungkannya ke jaringan Wi-Fi atau menggunakan alat yang telah siap pakai.</p>


